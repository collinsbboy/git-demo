							<span>About Regent University</span>

							<p>The Regent University College of Science and Technology (often referred to as Regent-Ghana) is located in Accra, Ghana. It was registered in September 2003, and received accreditation to operate as a tertiary institution in 2004. In January 2005 it started its lectures with 30 pioneer students at Trinity Campus, Mataheko. Now, the University operates from its purpose built campus, located at McCarthy Hill, off the Mallam-Kasoa-Winneba Highway. It is recognized as one of the leading private science and technology Universities in Ghana. In the 14th edition of the Webometrics Ranking of World Universities, Regent was ranked third best university in Ghana. Regent University College of Science and Technology was ranked the best private University in Ghana in the July 2020 edition of the Webometrics Ranking of World Universities. Regent is ranked 7th University overall in Ghana in the same edition of the bi-annual ranking.

                                The University College has four schools - the School of Business. Leadership and Legal Studies (SBLL), the Faculty of Arts and Sciences : the Faculty of Engineering, Computing and Allied Sciences (FECAS), and the Faculty of Arts and Social Sciences (FAS). The School of Research and Graduate Studies offers four postgraduate degree programmes - Master of Business Administration (MBA), Master of Science in Statistics, Master of Divinity, and Master of Theology.</p>

